<?php
// Якщо змінна $a більша нуля и менша 5-ти, то вивести 'Вірно', інакше вивести 'Невірно'.
// Перевірте роботу скрипта при $a, зі значеннями - 5, 0, -3, 2

echo ((check(5)) ? 'Вірно' : 'Невірно') . "<br>";
echo ((check(0)) ? 'Вірно' : 'Невірно') . "<br>";
echo ((check(-3)) ? 'Вірно' : 'Невірно') . "<br>";
echo ((check(2)) ? 'Вірно' : 'Невірно') . "<br>";

function check($a)
{
    return ($a > 0 && $a < 5) ? true : false;
}
