<?php
// Є масив з довільними числами.
// Зробіть так, щоб елемент повторився в масиві таку кількість разів
// яка відповідає його числу.
// Приклад: [1, 3, 2, 4] перетворюється в [1, 3, 3, 3, 2, 2, 4, 4, 4, 4].

$a = [1, 3, 2, 4];

function repeat($array)
{
    $result = [];
    for ($i = 0; $i < count($array); $i++) {
        for ($j = 0; $j < $array[$i]; $j++) {
            $result[] = $array[$i];
        }
    }
    return $result;
}

var_dump(repeat($a));